// Tipe Operator JavaScript


// ARITMATIKA (+, -, *, /, %(modulus))
2 + 9;
1 - 10;
5 * 5;
9 / 3;
100 % 2;

// PENUGASAN (assignment)( =, +=, -=, *=, /=, %= )
let a;
a = 10;

// PERANDINGAN-> hasilnya boolean
// == : sama dengan 
10 == 10;
// != : tidak sama dengan
10 != 10;
// === : strict sama dengan
10 === "10";
// !== : strict tidk sama dengan
10 !== "10";
// > : lebih besar dari
10 > 9;
// < : lebihkecil dari
9 < 2;
// >= : lebih besar sama dengan
9 >= 10;
// <= : lebih kecil sama dengan
2 <= 9;

// LOGIKA
// && : AND ( 2 kondisinya harus benar maka hasilnya true)
let a = 10;
let b = 20;
(a % 2 == 0) && (b % 2 == 0);
// || : OR ( salah satu kondisi benar hasilnya trus)
(a % 2 == 0) && (b % 3 == 0);
// ! : NOT ( membalikan nilai true jadi false dan false jadi true)
!(a % 2 == 0);

// STRING "+"
let f = "big";
let w = "dream";
f + w;
f + " " + w;

// typeof
typeof("fraza");

// Kondisional
// (Kondisi) ? true : false
(100 % 2 == 0) ? "benar" : "salah";